import io
import sqlite3
import numpy as np

# Functions that make saving and reading numpy arrays in sqlite possible
# Call these functions in a module whenever you use our sqlite database


def adapt_array(arr):
    """
    http://stackoverflow.com/a/31312102/190597 (SoulNibbler)
    """
    out = io.BytesIO()
    np.save(out, arr)
    out.seek(0)
    return sqlite3.Binary(out.read())


def convert_array(text):
    out = io.BytesIO(text)
    out.seek(0)
    return np.load(out)
